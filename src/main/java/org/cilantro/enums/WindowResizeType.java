package org.cilantro.enums;

/**
 * Window resize type.
 *
 * @author Rahul Saini
 * @since 31-Dec-2024
 */
public enum WindowResizeType {
    /**
     * Custom size type.
     */
    CUSTOM,
    /**
     * Full screen type.
     */
    FULL_SCREEN,
    /**
     * Maximum size type.
     */
    MAXIMIZED,
    /**
     * Minimum size type.
     */
    MINIMIZED,
    /**
     * Normal size type.
     */
    NORMAL,
}
