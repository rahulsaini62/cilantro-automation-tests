package org.cilantro.enums;

/**
 * Supported request methods.
 *
 * @author Rahul Saini
 * @since 04-Dec-2024
 */
public enum RequestMethod {
    /**
     * Delete request method.
     */
    DELETE,
    /**
     * Get request method.
     */
    GET,
    /**
     * Head request method.
     */
    HEAD,
    /**
     * Options request method.
     */
    OPTIONS,
    /**
     * Patch request method.
     */
    PATCH,
    /**
     * Post request method.
     */
    POST,
    /**
     * Put request method.
     */
    PUT,
    /**
     * Trace request method.
     */
    TRACE
}
